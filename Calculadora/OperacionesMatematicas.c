#include <math.h>

// function definition

/* 
    @param a primer elemento
    @param b segundo elemento
    @return a+b
*/
int Suma(int a, int b)
{
    return a + b;
}

/* 
    @param a primer elemento
    @param b segundo elemento
    @return a-b
*/
int Resta(int a, int b)
{
    return a - b;
}

/* 
    @param a primer elemento
    @param b segundo elemento
    @return a*b
*/
int Multiplicacion(int a, int b)
{
    return a * b;
}

/* 
    @param a primer elemento
    @param b segundo elemento
    @return a/b
*/
int Division (int a, int b)
{
    return a / b;
}

/* 
    @param a primer elemento
    @return raiz cuadrada de a
*/
int RaizCuadrada(int a)
{
    return sqrt(a);
}

/* 
    @param a primer elemento
    @return sen(a)
*/
int Seno(int a)
{
    return sin(a);
}

/* 
    @param a primer elemento
    @return cos(a)
*/
int Coseno(int a)
{
    return cos(a);
}

/* 
    @param a primer elemento
    @return tan(a)
*/
int Tangente(int a)
{
    return tan(a);
}

/* 
    @param a primer elemento
    @param b segundo elemento
    @return a^b
*/
int Potencia(int a, int b)
{
    return pow(a,b);
}